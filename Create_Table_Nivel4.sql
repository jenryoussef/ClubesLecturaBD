CREATE TABLE HIST_MEMBRESIAS(
    ID_CLUB NUMBER(3) CONSTRAINT NN_MEMBRESIA_ID_CLUB NOT NULL,
    ID_LECTOR NUMBER(5) CONSTRAINT NN_MEMBRESIA_ID_LECTOR NOT NULL,
    F_ING_CLUB DATE DEFAULT SYSDATE CONSTRAINT NN_MEMBRESIA_F_ING_CLUB NOT NULL,
    F_SOL_RETIRO DATE,
    F_RETIRO DATE,
    MOTIVO_RETIRO VARCHAR2(2) CONSTRAINT CHECK_MEMBRESIA_MOTIVO_RETIRO CHECK(MOTIVO_RETIRO IN ('DE', 'VO', 'IN', 'OT')),
    CONSTRAINT FK_MEMBRESIA_CLUB FOREIGN KEY(ID_CLUB) REFERENCES CLUBES(ID_CLUB),
    CONSTRAINT FK_MEMBRESIA_LECTOR FOREIGN KEY(ID_LECTOR) REFERENCES LECTORES(ID_LECTOR),
    CONSTRAINT PK_HIST_MEMBRESIA PRIMARY KEY(ID_CLUB, ID_LECTOR, F_ING_CLUB),
    CONSTRAINT CHECK_INGRESO_ANTES_SOLICITUD CHECK(F_SOL_RETIRO IS NULL OR F_ING_CLUB < F_SOL_RETIRO),
    CONSTRAINT CHECK_SOLICITUD_ANTES_RETIRO CHECK(F_SOL_RETIRO IS NULL OR F_RETIRO IS NULL OR F_SOL_RETIRO < F_RETIRO),
    CONSTRAINT CHECK_INGRESO_ANTES_RETIRO CHECK(F_RETIRO IS NULL OR F_ING_CLUB < F_RETIRO),
    CONSTRAINT CHECK_MOTIVO_RETIRO_NULL CHECK(
        (MOTIVO_RETIRO IS NULL AND F_RETIRO IS NULL) OR 
        (MOTIVO_RETIRO IS NOT NULL AND F_RETIRO IS NOT NULL)
    ),
    CONSTRAINT CHECK_SOL_RETIRO_NULL CHECK(
        (F_SOL_RETIRO IS NOT NULL AND NVL(MOTIVO_RETIRO, 'X') = 'VO') OR 
        (F_SOL_RETIRO IS NULL AND (MOTIVO_RETIRO IS NULL OR MOTIVO_RETIRO <> 'VO'))
    )
);

CREATE TABLE OBRAS_ACTUADAS(
    ID_CLUB NUMBER(3) CONSTRAINT NN_OBRA_ID_CLUB NOT NULL,
    ID_OBRA NUMBER(3) CONSTRAINT NN_OBRA_ID_OBRA NOT NULL,
    TITULO VARCHAR2(50) CONSTRAINT NN_OBRA_TITULO NOT NULL,
    DURACION_MINUTOS NUMBER(3) CONSTRAINT NN_OBRA_DURACION NOT NULL CONSTRAINT CHECK_OBRA_DURACION CHECK(DURACION_MINUTOS > 0),
    DESCRIPCION VARCHAR2(200) CONSTRAINT NN_OBRA_DESCRIPCION NOT NULL,
    ACTIVA VARCHAR2(1) CONSTRAINT NN_OBRA_ACTIVA NOT NULL CONSTRAINT CHECK_OBRA_ACTIVA CHECK(ACTIVA = 'S' OR ACTIVA = 'N'),
    COSTO NUMBER(6, 2) CONSTRAINT CHECK_OBRA_COSTO CHECK(COSTO >= 0),
    CONSTRAINT FK_OBRA_CLUB FOREIGN KEY(ID_CLUB) REFERENCES CLUBES(ID_CLUB),
    CONSTRAINT PK_OBRA PRIMARY KEY(ID_CLUB, ID_OBRA)
);

CREATE TABLE ASOCIACIONES(
