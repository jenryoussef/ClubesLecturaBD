CREATE TABLE TELEFONOS(
    CODIGO_PAIS NUMBER(3) CONSTRAINT NN_TELEFONO_CODIGO_PAIS NOT NULL CONSTRAINT CHECK_TELEFONO_CODIGO_PAIS CHECK(CODIGO_PAIS > 0),
    CODIGO_AREA NUMBER(5) CONSTRAINT NN_TELEFONO_CODIGO_AREA NOT NULL CONSTRAINT CHECK_TELEFONO_CODIGO_AREA CHECK(CODIGO_AREA > 0),
    NUMERO NUMBER(9) CONSTRAINT NN_TELEFONO_NUMERO NOT NULL CONSTRAINT CHECK_TELEFONO_NUMERO CHECK(NUMERO > 0),
    ID_LECTOR NUMBER(5),
    ID_CLUB NUMBER(3),
    CONSTRAINT PK_TELEFONO PRIMARY KEY (CODIGO_PAIS, CODIGO_AREA, NUMERO),
    CONSTRAINT FK_TELEFONO_LECTOR FOREIGN KEY(ID_LECTOR) REFERENCES LECTORES(ID_LECTOR),
    CONSTRAINT FK_TELEFONO_CLUB FOREIGN KEY(ID_CLUB) REFERENCES CLUBES(ID_CLUB),
    CONSTRAINT CHECK_ARCO_TELEFONO CHECK(
        (ID_LECTOR IS NULL AND ID_CLUB IS NOT NULL) OR
        (ID_LECTOR IS NOT NULL AND ID_CLUB IS NULL)
    )
);

